def print_rainbowed(text):
    for letter_part in letters:
        c = 1
        for l in text:
            if l == " ":
                print("    ", end="")
            else:
                print(f" {colors[c]}{letter_part[ord(l)-ord('a')]}{colors[0]} ", end="")
                c = c+1 if c<7 else 1
        print()


